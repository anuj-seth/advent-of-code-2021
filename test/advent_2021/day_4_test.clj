(ns advent-2021.day-4-test
  (:require [clojure.test :refer :all]
            [advent-2021.day-4 :as day-4]
            [advent-2021.utils :as utils]))


(deftest day-4-test
  (let [string-to-vector #(read-string (format "[%s]"
                                               %))
        lines (utils/input-lines "day_4.txt")
        numbers-drawn (string-to-vector (first lines))
        boards (->> (rest lines)
                    (remove empty?)
                    (map string-to-vector)
                    (partition 5))]
    (testing "day 4 part 1"
      (is (false? (day-4/row-marked? [[:* :* :* :*  24]
                                      [10 16 15  :* 19]
                                      [18  8 :* 26 20]
                                      [22 :* 13  6  :*]
                                      [:*  :* 12  3  :*]])))
      (is (true? (day-4/row-marked? [[:* :* :* :*  :*]
                                     [10 16 15  :* 19]
                                     [18  8 :* 26 20]
                                     [22 :* 13  6  :*]
                                     [:*  :* 12  3  :*]])))
      (is (false? (day-4/column-marked? [[:* :* :* :*  24]
                                         [10 16 15  :* 19]
                                         [18  8 :* 26 20]
                                         [22 :* 13  6  :*]
                                         [:*  :* 12  3  :*]])))
      (is (true? (day-4/column-marked? [[:* :* :* :*  :*]
                                        [:* 16 15  :* 19]
                                        [:*  8 :* 26 20]
                                        [:* :* 13  6  :*]
                                        [:*  :* 12  3  :*]])))
      (is (true? (day-4/column-marked? [[1 :* :* :* :*]
                                        [:* 16 :* :* 19]
                                        [:* 8 :* 26 20]
                                        [:* :* :* 6 :*]
                                        [:* :* :* 3 :*]])))
      (is (= '(([:* :* :* :* :*]
                [:* :* 15 :* 19]
                [:* 8 :* 26 20]
                [:* :* 13 6 :*]
                [:* :* 12 3 :*])
               ([:* 15 :* 15]))
             (day-4/mark-boards [[[:* :* :* :*  :*]
                                  [:* 16 15  :* 19]
                                  [:*  8 :* 26 20]
                                  [:* :* 13  6  :*]
                                  [:*  :* 12  3  :*]]
                                 [[16 15 16 15]]]
                                16)))
      (is (= 4512 (day-4/score [[:* :* :* :*  :*]
                                [10 16 15  :* 19]
                                [18  8 :* 26 20]
                                [22 :* 13  6  :*]
                                [:*  :* 12  3  :*]]
                               24)))
      (is (= 4512 (day-4/bingo [7 4 9 5 11 17 23 2 0 14 21 24 10 16 13 6 15 25 12 22 18 20 8 19 3 26 1]
                               [[[22 13 17 11  0]
                                 [8  2 23  4 24]
                                 [21  9 14 16  7]
                                 [6 10  3 18  5]
                                 [1 12 20 15 19]]
                                [[3 15  0  2 22]
                                 [9 18 13 17  5]
                                 [19  8  7 25 23]
                                 [20 11 10 24  4]
                                 [14 21 16 12  6]]
                                [[14 21 17 24  4]
                                 [10 16 15  9 19]
                                 [18  8 23 26 20]
                                 [22 11 13  6  5]
                                 [2  0 12  3  7]]])))
      (is (= 10374 (day-4/bingo numbers-drawn
                                boards))))
    (testing "day 4 part 2"
        (is (= 1924 (day-4/bingo-loser [7 4 9 5 11 17 23 2 0 14 21 24 10 16 13 6 15 25 12 22 18 20 8 19 3 26 1]
                                       [[[22 13 17 11  0]
                                         [8  2 23  4 24]
                                         [21  9 14 16  7]
                                         [6 10  3 18  5]
                                         [1 12 20 15 19]]
                                        [[3 15  0  2 22]
                                         [9 18 13 17  5]
                                         [19  8  7 25 23]
                                         [20 11 10 24  4]
                                         [14 21 16 12  6]]
                                        [[14 21 17 24  4]
                                         [10 16 15  9 19]
                                         [18  8 23 26 20]
                                         [22 11 13  6  5]
                                         [2  0 12  3  7]]]))))
    (is (= 24742 (day-4/bingo-loser numbers-drawn
                                    boards)))))

